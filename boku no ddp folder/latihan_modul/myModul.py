import math

def pangkat(bil1,bil2):
    hasil = math.pow(bil1,bil2)
    print("Hasil pemangkatan ",bil1," dan ",bil2, " = ",hasil)

def akar(bilangan):
    hasil = math.sqrt(bilangan)
    print("Hasil akar dari ",bilangan," = ",hasil)

def perkalian(bil1,bil2):
    hasil = bil1 * bil2
    print("Hasil dari perkalian ",bil1," dan ",bil2," = ",hasil)
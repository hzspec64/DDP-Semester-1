#Looping while dgn if & operator modulus (sisa bagi) %
print("------------Bilangan Ganjil---------------------\n")
bil = 1
while(bil <= 5):
    if(bil % 2 == 1):
        print("Bilangan Ganjil ",bil)
    bil += 1
print("\n------------Bilangan Genap---------------------\n")
bil = 1
while(bil <= 5):
    if(bil % 2 == 0):
        print("Bilangan genap ",bil)
    bil += 1